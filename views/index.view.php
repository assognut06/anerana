            <h1 class="mb-4 text-primary">ANERANA : Une Nouvelle Association pour la Mémoire des Rapatriés d'Afrique du Nord</h1>
            <p class="fs-4"><strong>Nice, le 8 octobre 2024</strong> - Une nouvelle association vient de voir le jour dans les Alpes-Maritimes, dédiée à la préservation et à la transmission des cultures des rapatriés d'Afrique du Nord. L'Association Nationale des Enfants de Rapatriés d'Afrique du Nord et leurs Amis (ANERANA) a été officiellement déclarée à la préfecture des Alpes-Maritimes le 30 septembre 2024.</p>
            <h2 class="text-secondary">Un Objectif Clair : Promouvoir et Transmettre</h2>
            <p class="fs-4">L'ANERANA se fixe pour objectif de promouvoir les cultures « Pieds-Noirs » et rapatriées d’Afrique du Nord. Cette mission s'inscrit dans une perspective d’échanges et de transmission aux générations actuelles et futures. Pour ce faire, l'association prévoit une série d'initiatives variées :</p>
            <ul>
                <li class="fs-4"><strong>Organisation d’événements culturels et mémoriels</strong> : Rencontres, conférences, colloques, expositions, spectacles, festivités, commémorations, sorties et voyages sont au programme pour renforcer les liens entre les deux rives de la Méditerranée.</li>
                <li class="fs-4"><strong>Compilation et partage de patrimoine</strong> : Documents, témoignages, objets et autres éléments du patrimoine matériel et immatériel seront mis en valeur et partagés.</li>
                <li class="fs-4"><strong>Créations artistiques et littéraires</strong> : L'association encouragera les créations artistiques et littéraires, ainsi que les publications et diffusions.</li>
                <li class="fs-4"><strong>Partenariats</strong> : Des collaborations avec d’autres associations, institutions et entreprises seront établies pour enrichir les initiatives de l'ANERANA.</li>
                <li class="fs-4"><strong>Journée nationale de la transmission</strong> : Une journée dédiée à la transmission des cultures et de la mémoire des Pieds-Noirs et rapatriés d’Afrique du Nord sera instaurée.</li>
            </ul>
            <h2 class="text-secondary">Un Engagement Économique et Social</h2>
            <p class="fs-4">L'ANERANA ne se limite pas aux activités culturelles. L'association envisage également des activités économiques telles que la vente d’œuvres, de publications, de spectacles, de produits et d’objets, ainsi que des services de restauration. Ces initiatives économiques visent à soutenir les actions de l'association tout en respectant les valeurs de bienveillance, de neutralité politique et religieuse, et de respect des valeurs laïques.</p>
            <h2 class="text-secondary">Siège Social et Perspectives</h2>
            <p class="fs-4">Le siège social de l'ANERANA est situé à la Maison des Associations de Rapatriés, 16, rue Bavastro, à Nice. Avec un programme ambitieux et une vision claire, l'ANERANA s'apprête à jouer un rôle crucial dans la préservation et la transmission des cultures des rapatriés d'Afrique du Nord.</p>
            <p class="fs-4">Pour plus d'informations, les intéressés peuvent se rendre sur le site officiel de l'association ou contacter directement le siège social.</p>
